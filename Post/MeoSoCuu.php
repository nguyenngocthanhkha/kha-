  <h1>🩺 Mẹo Sơ Cứu Cần Biết Khi Du Lịch Tại Bình Định</h1>

  <p>Khi đi du lịch, đặc biệt là đến những nơi hoang sơ như biển, đồi núi ở Bình Định, bạn có thể gặp phải những tình huống không mong muốn như trầy xước, say nắng, côn trùng cắn hay thậm chí bị ngã nhẹ. Dưới đây là một số mẹo sơ cứu cơ bản giúp bạn xử lý nhanh, đảm bảo chuyến đi luôn an toàn và vui vẻ.</p>

  <h2>🌞 1. Sơ Cứu Khi Bị Say Nắng</h2>
  <img src="images/say-nang.jpg" alt="Du khách bị say nắng" style="width:100%;max-width:600px;margin-bottom:10px;">
  <ul>
    <li>🥤 Đưa người bị say nắng vào nơi mát, thoáng gió.</li>
    <li>💧 Cho uống nước từ từ, có thể thêm ít muối hoặc oresol.</li>
    <li>🧊 Chườm khăn mát ở trán, cổ, nách để hạ nhiệt.</li>
  </ul>
  <p><strong>Lưu ý:</strong> Không nên tắm nước lạnh đột ngột khi đang say nắng!</p>

  <h2>🦟 2. Xử Lý Vết Côn Trùng Cắn</h2>
  <img src="images/con-trung-can.jpg" alt="Côn trùng cắn khi đi rừng" style="width:100%;max-width:600px;margin-bottom:10px;">
  <ul>
    <li>🧼 Rửa sạch vùng bị cắn bằng nước và xà phòng.</li>
    <li>❄️ Chườm đá nếu sưng tấy hoặc ngứa.</li>
    <li>💊 Thoa kem chống ngứa hoặc thuốc dị ứng nhẹ nếu có.</li>
  </ul>
  <p>Nếu có dấu hiệu dị ứng nặng (sưng lan rộng, khó thở), cần đến cơ sở y tế ngay.</p>

  <h2>🩹 3. Sơ Cứu Vết Thương Nhẹ (Trầy Xước, Đứt Tay)</h2>
  <img src="images/tray-xuoc.jpg" alt="Xử lý vết trầy xước" style="width:100%;max-width:600px;margin-bottom:10px;">
  <ul>
    <li>🧴 Rửa sạch vết thương bằng nước sạch hoặc nước muối sinh lý.</li>
    <li>🧼 Dùng bông/gạc lau nhẹ nhàng và sát khuẩn bằng betadine hoặc cồn y tế 70°.</li>
    <li>🩹 Băng lại bằng băng gạc sạch, thay băng mỗi ngày.</li>
  </ul>

  <h2>🤢 4. Khi Bị Ngộ Độc Thực Phẩm Nhẹ</h2>
  <img src="images/ngo-doc-thuc-pham.jpg" alt="Ngộ độc thực phẩm khi đi du lịch" style="width:100%;max-width:600px;margin-bottom:10px;">
  <ul>
    <li>🚱 Ngưng ăn uống tạm thời để theo dõi.</li>
    <li>💧 Uống nhiều nước, đặc biệt là oresol để bù điện giải.</li>
    <li>🛌 Nghỉ ngơi và tránh di chuyển nhiều.</li>
  </ul>
  <p>Nếu tiêu chảy, sốt cao, nôn mửa kéo dài, cần đưa đi bệnh viện ngay.</p>

  <h2>🎒 5. Mang Theo Bộ Sơ Cứu Du Lịch</h2>
  <img src="images/bo-so-cuu.jpg" alt="Bộ sơ cứu cá nhân khi đi du lịch" style="width:100%;max-width:600px;margin-bottom:10px;">
  <p>Một bộ sơ cứu gọn nhẹ bạn nên chuẩn bị khi du lịch Bình Định gồm:</p>
  <ul>
    <li>🧴 Dung dịch rửa vết thương (nước muối sinh lý, cồn y tế)</li>
    <li>🩹 Băng keo cá nhân, gạc, băng cuốn</li>
    <li>💊 Thuốc đau đầu, tiêu hóa, dị ứng</li>
    <li>🦟 Kem chống muỗi, kem chống nắng</li>
    <li>😷 Khẩu trang y tế, găng tay dùng một lần</li>
  </ul>

