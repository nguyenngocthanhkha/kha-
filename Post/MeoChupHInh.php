  <h1>📸 Mẹo Chụp Hình Đẹp Khi Du Lịch Bình Định</h1>

  <p>Bình Định – mảnh đất võ trời văn, không chỉ nổi tiếng với bề dày lịch sử và văn hóa, mà còn khiến du khách mê mẩn bởi những cảnh đẹp hoang sơ, thơ mộng. Nếu bạn đang lên kế hoạch du lịch Bình Định và muốn lưu giữ những khoảnh khắc thật lung linh, hãy bỏ túi ngay những mẹo chụp hình dưới đây!</p>

  <h2>⏰ 1. Chọn Thời Gian Vàng Để Chụp Ảnh</h2>
  <img src="images/binh-minh-binh-dinh.jpg" alt="Bình minh tại biển Bình Định" style="width:100%;max-width:600px;margin-bottom:10px;">
  <p>Ánh sáng là yếu tố quan trọng nhất khi chụp hình. Ở Bình Định, khung giờ "vàng" để có ảnh đẹp là:</p>
  <ul>
    <li>🌅 <strong>Sáng sớm (5h30 – 7h00):</strong> Ánh nắng nhẹ, không gắt, tạo cảm giác dịu dàng.</li>
    <li>🌇 <strong>Chiều hoàng hôn (16h30 – 18h00):</strong> Rất lý tưởng để chụp tại biển, đồi núi hoặc các địa điểm hoang sơ như Eo Gió, Kỳ Co, hoặc đồi cát Phương Mai.</li>
  </ul>

  <h2>🌍 2. Tận Dụng Địa Hình Đa Dạng</h2>
  <img src="images/eo-gio-ky-co.jpg" alt="Eo Gió và Kỳ Co" style="width:100%;max-width:600px;margin-bottom:10px;">
  <p>Bình Định có đủ loại cảnh quan: biển, núi, đồi, làng chài, tháp Chăm, ruộng lúa... Hãy khai thác đa dạng địa hình để có những album phong phú:</p>
  <ul>
    <li>🌊 <strong>Biển Kỳ Co, Eo Gió:</strong> Hướng ra đại dương, nắng trong veo, sóng biển xanh ngắt.</li>
    <li>🏛️ <strong>Tháp Bánh Ít, Tháp Đôi:</strong> Mang màu sắc cổ kính, rất hợp với concept retro hoặc du lịch văn hóa.</li>
    <li>🚣 <strong>Làng chài Nhơn Lý:</strong> Đời sống người dân, thuyền thúng, chợ cá sáng – rất “đời” và độc đáo.</li>
  </ul>

  <h2>👗 3. Trang Phục Phù Hợp Với Khung Cảnh</h2>
  <img src="images/trang-phuc-du-lich.jpg" alt="Trang phục du lịch biển" style="width:100%;max-width:600px;margin-bottom:10px;">
  <p>Hãy chọn quần áo phù hợp để nổi bật trong khung hình:</p>
  <ul>
    <li>👒 <strong>Biển:</strong> Váy maxi, đồ màu trắng hoặc pastel.</li>
    <li>📷 <strong>Di tích:</strong> Áo dài, váy vintage, trang phục tông trầm.</li>
    <li>🥾 <strong>Đồi cát/núi:</strong> Quần áo thể thao hoặc năng động.</li>
  </ul>
  <p>Đừng quên mang theo nón cói, khăn choàng, hoặc phụ kiện nhỏ để tạo điểm nhấn cho ảnh nhé!</p>

  <h2>🎯 4. Canh Góc Và Bố Cục Hợp Lý</h2>
  <img src="images/goc-chup.jpg" alt="Bố cục và góc chụp hợp lý" style="width:100%;max-width:600px;margin-bottom:10px;">
