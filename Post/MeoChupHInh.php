    <li>📐 <strong>Góc thấp:</strong> Giúp bạn trông cao hơn và cho khung cảnh phía sau rộng hơn.</li>
    <li>🛣️ <strong>Đường dẫn:</strong> Tận dụng những con đường, hàng cây, cầu gỗ để dẫn mắt vào chủ thể.</li>
    <li>📏 <strong>Bố cục 1/3:</strong> Đặt bạn hoặc chủ thể vào 1/3 khung hình, tránh để ở giữa.</li>
  </ul>

